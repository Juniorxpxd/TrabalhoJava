package Banco.model;

public class Agencia {
	private int agencia;
	private int conta;
	
	public int getAgencia() {
		return agencia;
	}
	public int getConta() {
		return conta;
	}
	public void setAgencia(int agencia) {
		this.agencia = agencia;
	}
	public void setConta(int conta) {
		this.conta = conta;
	}
	
	
}
